def load_member_data(dataset_name: str, batch_size: int = 4):
    """
    Returns (member_set, nonmember_set, member_loader, nonmember_loader).
    Supported dataset_name values: 'laion5', 'laion5_blip'
    """
    transform = transforms.Compose([
        transforms.Resize(512, interpolation=transforms.InterpolationMode.BILINEAR),
        transforms.CenterCrop(512),
        transforms.ToTensor(),
        transforms.Normalize([0.5], [0.5]),   # → [-1, 1]
    ])

    if dataset_name == "laion5":
        member_set = Laion5(
            f"{SD_DATA_DIR}/images-random",
            f"{SD_DATA_DIR}/val-list-2500-random.npy",
            transform=transform,
        )
        nonmember_set = CocoDetection(
            root=COCO_IMG_DIR, annFile=COCO_ANN_FILE, transform=transform,
        )

    elif dataset_name == "laion5_blip":
        member_set = Laion5Generated(
            f"{SD_DATA_DIR}/images-random",
            f"{SD_DATA_DIR}/val-list-2500-random.npy",
            f"{SD_DATA_DIR}/text_generation/images-random.csv",
            transform=transform,
        )
        nonmember_set = CocoDetectionGenerated(
            root=COCO_IMG_DIR,
            annFile=COCO_ANN_FILE,
            generated=f"{SD_DATA_DIR}/text_generation/val2017.csv",
            transform=transform,
        )
    else:
        raise ValueError(f"Unknown dataset_name: {dataset_name!r}. "
                         "Choose 'laion5' or 'laion5_blip'.")

    member_loader    = torch.utils.data.DataLoader(
        member_set,    batch_size=batch_size, collate_fn=member_set.collate_fn
    )
    nonmember_loader = torch.utils.data.DataLoader(
        nonmember_set, batch_size=batch_size, collate_fn=nonmember_set.collate_fn
    )
    return member_set, nonmember_set, member_loader, nonmember_loader
