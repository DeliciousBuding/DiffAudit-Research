SD_DATA_DIR   = os.environ.get("SD_MIA_DATA_DIR", "stable_diffusion_data")


def np_load_pickle_compat(path):
    """
    Load object .npy files saved by NumPy 2.x from a NumPy 1.x environment.
    The compatibility aliases are kept local to np.load so C extensions such as
    pycocotools do not see a synthetic numpy._core module later.
    """
    added = []
    if not hasattr(np, "_core"):
        aliases = {
            "numpy._core": np.core,
            "numpy._core.multiarray": np.core.multiarray,
            "numpy._core.numeric": np.core.numeric,
        }
        for name, module in aliases.items():
            if name not in sys.modules:
                sys.modules[name] = module
                added.append(name)
    try:
        return np.load(path, allow_pickle=True)
    finally:
        for name in added:
            sys.modules.pop(name, None)


class Laion5(VisionDataset):
    """LAION member set with ground-truth text captions."""

    def __init__(self, root, metadata, transforms=None, transform=None, target_transform=None):
        super().__init__(root, transforms, transform, target_transform)
        self.metadata = np_load_pickle_compat(metadata)

    def __getitem__(self, index: int) -> Any:
        img = Image.open(
            os.path.join(self.root, self.metadata[index][0] + ".jpg")
        ).convert("RGB")
        caption = self.metadata[index][1]
        if self.transforms is not None:
            img, caption = self.transforms(img, caption)
        return img, caption

    def __len__(self) -> int:
        return len(self.metadata)

    @staticmethod
    def collate_fn(examples):
        imgs     = [e[0] for e in examples]
        captions = [e[1] for e in examples]
        return torch.stack(imgs), captions


class Laion5Generated(Laion5):
    """LAION member set with BLIP-generated captions."""

    def __init__(self, root, metadata, generated, transforms=None, transform=None, target_transform=None):
        super().__init__(root, metadata, transforms, transform, target_transform)
        self.generated = pd.read_csv(generated)

    def __getitem__(self, index: int) -> Any:
        img = Image.open(
            os.path.join(self.root, self.metadata[index][0] + ".jpg")
        ).convert("RGB")
        file_name = self.metadata[index][0] + ".jpg"
        caption = self.generated[self.generated.file_name == file_name].iloc[0, 0]
        if self.transforms is not None:
            img, caption = self.transforms(img, caption)
        return img, caption


class CocoDetection(_CocoDetection):
    """COCO2017-val nonmember set — uses a pre-selected 2500-image split."""

    def __init__(self, root, annFile, transform=None, target_transform=None, transforms=None):
        super().__init__(root, annFile, transform, target_transform, transforms)
        self.splited_id = OmegaConf.load(COCO_SPLIT_YAML)

    def __len__(self) -> int:
        return len(self.splited_id)

    def __getitem__(self, index: int):
        return super().__getitem__(self.splited_id[index])

    @staticmethod
    def collate_fn(examples):
        imgs     = [e[0] for e in examples]
        captions = [e[1][0]["caption"] for e in examples]
        return torch.stack(imgs), captions


